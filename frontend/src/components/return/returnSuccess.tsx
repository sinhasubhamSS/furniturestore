"use client";

import React from "react";
import { useCheckEligibilityQuery } from "@/redux/services/user/returnApi";

interface ReturnEligibilityProps {
  orderId: string;
  onEligible: (order: any) => void;
  onNotEligible: (reason: string) => void;
}

export default function ReturnEligibility({
  orderId,
  onEligible,
  onNotEligible,
}: ReturnEligibilityProps) {
  const { data, isLoading, error } = useCheckEligibilityQuery({ orderId });

  React.useEffect(() => {
    if (data?.data?.isEligible) {
      onEligible(data.data.order);
    } else if (data?.data && !data.data.isEligible) {
      onNotEligible(data.data.reason);
    }
  }, [data, onEligible, onNotEligible]);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center py-8">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-blue-600"></div>
        <span className="ml-2">Checking return eligibility...</span>
      </div>
    );
  }

  if (error) {
    return (
      <div className="bg-red-50 border border-red-200 rounded-lg p-4">
        <div className="flex items-center">
          <div className="flex-shrink-0">
            <svg
              className="h-5 w-5 text-red-400"
              viewBox="0 0 20 20"
              fill="currentColor"
            >
              <path
                fillRule="evenodd"
                d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z"
                clipRule="evenodd"
              />
            </svg>
          </div>
          <div className="ml-3">
            <h3 className="text-sm font-medium text-red-800">
              Error checking eligibility
            </h3>
            <p className="text-sm text-red-700 mt-1">Please try again later</p>
          </div>
        </div>
      </div>
    );
  }

  if (data?.data?.isEligible) {
    return (
      <div className="bg-green-50 border border-green-200 rounded-lg p-4 mb-6">
        <div className="flex items-center">
          <div className="flex-shrink-0">
            <svg
              className="h-5 w-5 text-green-400"
              viewBox="0 0 20 20"
              fill="currentColor"
            >
              <path
                fillRule="evenodd"
                d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z"
                clipRule="evenodd"
              />
            </svg>
          </div>
          <div className="ml-3">
            <h3 className="text-sm font-medium text-green-800">
              Order eligible for return
            </h3>
            <p className="text-sm text-green-700 mt-1">
              Time remaining:{" "}
              {Math.ceil(data.data.timeRemaining / (1000 * 60 * 60 * 24))} days
            </p>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="bg-red-50 border border-red-200 rounded-lg p-4">
      <div className="flex items-center">
        <div className="flex-shrink-0">
          <svg
            className="h-5 w-5 text-red-400"
            viewBox="0 0 20 20"
            fill="currentColor"
          >
            <path
              fillRule="evenodd"
              d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z"
              clipRule="evenodd"
            />
          </svg>
        </div>
        <div className="ml-3">
          <h3 className="text-sm font-medium text-red-800">
            Not eligible for return
          </h3>
          <p className="text-sm text-red-700 mt-1">{data?.data?.reason}</p>
        </div>
      </div>
    </div>
  );
}
